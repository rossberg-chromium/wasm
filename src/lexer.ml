let memop d a s t =
  let dist = match d with
    | "near" -> Near
    | "far" -> Far
    | _ -> assert false
  and align = match a with
    | "" -> Memory.Aligned
    | "unaligned" -> Memory.Unaligned
    | _ -> assert false
  in {dist; align; mem = mem_type s t}


let convert_escape = function
  | 'n' -> '\n'
  | 't' -> '\t'
  | '\\' -> '\\'
  | '\'' -> '\''
  | '\"' -> '\"'
  | _ -> assert false

let convert_text s =
  let b = Buffer.create (String.length s) in
  let i = ref 1 in
  while !i < String.length s - 1 do
    Buffer.add_char b
      (if s.[!i] <> '\\' then s.[!i] else (incr i; convert_escape s.[!i]));
    incr i
  done;
  Buffer.contents b

let value_type = function
  | "i32" -> Int32Type
  | "i64" -> Int64Type
  | "f32" -> Float32Type
  | "f64" -> Float64Type
  | _ -> assert false

let mem_type s t =
  let open Memory in
  match s, t with
  | 's', "i8" -> SInt8Mem
  | 's', "i16" -> SInt16Mem
  | 's', "i32" -> SInt32Mem
  | 's', "i64" -> SInt64Mem
  | 'u', "i8" -> UInt8Mem
  | 'u', "i16" -> UInt16Mem
  | 'u', "i32" -> UInt32Mem
  | 'u', "i64" -> UInt64Mem
  | ' ', "f32" -> Float32Mem
  | ' ', "f64" -> Float64Mem
  | _ -> assert false

